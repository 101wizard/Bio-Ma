class LabAssistant:
    def __init__(self, la_id, la_name, la_phone, la_email, la_img):
        self.la_id = la_id  # INT
        self.la_name = la_name  # VARCHAR(255)
        self.la_phone = la_phone  # VARCHAR(15)
        self.la_email = la_email  # VARCHAR(255)
        self.la_img = la_img  # BLOB